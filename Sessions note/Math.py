# some maths functions
a = 11
b = 3
# addition
sum = a + b
print(sum)

# subtraction
sub = a - b
print(sub)

# multiplication
mult = a * b
print(mult)

# division
div = a / b
print(div)  # Output: 3.6666666666666665

# Quotient
quo = a // b  # Quotient is lower value of division(3.666)   
print(quo)

# remainder
rem = a % b
print(rem)

# exponentiation means power ex: 2 to the power 4
c = 2
d = 4
exp = c ** d
print(exp)  

# some maths functions using math module
